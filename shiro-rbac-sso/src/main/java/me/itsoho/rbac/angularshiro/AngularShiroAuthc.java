package me.itsoho.rbac.angularshiro;


public class AngularShiroAuthc {
	private AngularShiroPrincipal principal;
	private AngularShiroCredentials credentials;
	public AngularShiroPrincipal getPrincipal() {
		return principal;
	}
	public void setPrincipal(AngularShiroPrincipal principal) {
		this.principal = principal;
	}
	public AngularShiroCredentials getCredentials() {
		return credentials;
	}
	public void setCredentials(AngularShiroCredentials credentials) {
		this.credentials = credentials;
	}


	
	
}
